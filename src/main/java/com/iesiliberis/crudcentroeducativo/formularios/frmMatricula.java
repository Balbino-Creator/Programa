    private void cargaTabla(){
        modelo=(DefaultTableModel)jtMatriculas.getModel();
        frmUnidades h_id = new frmUnidades();//Creamos un nuevo form para obtener la id de Unidad
        idUnidad = h_id.idUnidad;
        MatriculaDaoImp matriculaControler=MatriculaDaoImp.getInstance();
        String[] fila=new String[5];
        
        modelo.setNumRows(0);
        try{
            List<Matricula> lst=matriculaControler.getAllByUnidad(idUnidad);//Obtenemos la id desde frmMatricula
            
            for( Matricula matricula :lst){
                fila[0]=""+matricula.getIdmatricula();
                fila[1]=""+matricula.getDescripcion();
                fila[2]=""+matricula.getfMatricula();
                fila[3]=""+matricula.getfBaja();
                
                modelo.addRow(fila);
            }
            //selecciono la primera fila
           jtMatriculas.setRowSelectionInterval(0,0); 
           
        }catch(Exception e){
            System.out.println("Error:"+e.getMessage());
        }
    }
