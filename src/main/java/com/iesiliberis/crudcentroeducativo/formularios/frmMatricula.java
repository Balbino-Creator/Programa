    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        // TODO add your handling code here:
        Matricula m = new Matricula();
        MatriculaDaoImp cdi = MatriculaDaoImp.getInstance();

        //añadimos los datos de los campos de texto
        
        try{
            m.setDescripcion(txtDescripcion.getText());
        } catch(Exception e){
            m.setDescripcion(null);
        }
        
        m.setfMatricula(Date.valueOf(txtFMatricula.getText()));
        
        try{
           m.setfBaja(Date.valueOf(txtFBaja.getText())); 
        }catch(Exception e){
            m.setfBaja(null);
        }
        
        m.setIdalumno(idAlumno);
        m.setIdunidad(idUnidad);

        try {
            //añadimos a la base de datos el nuevo curso
            cdi.add(m);
            cargaTabla();
        } catch (Exception e) {
            System.out.println("Error:"+e.getMessage());
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

